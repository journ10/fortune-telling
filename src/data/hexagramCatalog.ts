const LINE_POSITIONS = [1, 2, 3, 4, 5, 6] as const satisfies readonly HexagramLineText['position'][];
const LINE_NUMERALS = ['初', '二', '三', '四', '五', '上'] as const;
const STAGE_LABELS = ['初位', '二位', '三位', '四位', '五位', '上位'] as const;
const STAGE_TAGS = ['蓄势', '守中', '转折', '进退', '成局', '收束'] as const;

function makeLineTitle(index: number, isYang: boolean): string {
  const lineKind = isYang ? '九' : '六';

  if (index === 0) {
    return `${LINE_NUMERALS[index]}${lineKind}`;
  }

  if (index === 5) {
    return `${LINE_NUMERALS[index]}${lineKind}`;
  }

  return `${lineKind}${LINE_NUMERALS[index]}`;
}

function makeLines(seed: CatalogSeed): HexagramLineText[] {
  const lineTexts = CANONICAL_LINE_TEXTS[seed.id];

  if (!lineTexts) {
    throw new Error(`Missing canonical line text for hexagram ${seed.id}`);
  }

  return LINE_POSITIONS.map((position, index) => {
    const isYang = seed.pattern[index] === '1';

    return {
      position,
      title: makeLineTitle(index, isYang),
      original: lineTexts[index],
      summary: `${STAGE_LABELS[index]}提示在${seed.name}中重视${STAGE_TAGS[index]}，避免把爻辞理解为绝对预断。`,
      tags: [STAGE_TAGS[index], isYang ? '阳爻' : '阴爻']
    };
  });
}

function catalogEntry(seed: CatalogSeed): HexagramCatalogEntry {
  return {
    ...seed,
    lines: makeLines(seed)
  };
}

